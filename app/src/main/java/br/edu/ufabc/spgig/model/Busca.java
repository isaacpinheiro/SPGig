package br.edu.ufabc.spgig.model;

public class Busca {

    private String tipo;
    private String cidade;

    public Busca() {

    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

}
